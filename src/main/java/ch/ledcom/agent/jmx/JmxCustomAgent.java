package ch.ledcom.agent.jmx;

import java.io.IOException;
import java.lang.management.ManagementFactory;
import java.net.InetAddress;
import java.rmi.registry.LocateRegistry;
import java.util.HashMap;
import javax.management.MBeanServer;
import javax.management.remote.JMXConnectorServer;
import javax.management.remote.JMXConnectorServerFactory;
import javax.management.remote.JMXServiceURL;

/**
 * This CustomAgent will start an RMI Connector Server using only port
 * "ch.ledcom.agent.jmx.port". When used with the javaagent option, it
 * allows a remote connection using JConsole or VisualVM through an SSH tunnel.
 * 
 * The purpose of this agent is to open a JMX server that will use only one
 * fixed port for all communication instead of two dynamically set ports.
 * 
 * Code adapted from:
 * http://blogs.sun.com/jmxetc/entry/connecting_through_firewall_using_jmx
 * 
 * @author gehel
 */
public class JmxCustomAgent {

	private static final String BASE = "ch.ledcom.agent.jmx.";
	private static final String PORT_KEY = BASE + "port";
	private static final String FORCE_LOCALHOST_KEY = BASE + "forceLocalhost";
	private static final String PASSWORD_FILE = BASE + "password.file";
	private static final String ACCESS_FILE = BASE + "access.file";

	private JmxCustomAgent() {
	}

	public static void premain(String agentArgs) throws IOException {

		String portStr = System.getProperty(PORT_KEY);
		if ((portStr == null) || portStr.isEmpty()) {
			System.err.println();
			System.err.println();
			System.err
					.println("******************************************************");
			System.err.println("*");
			System.err.println("* RMI / JMX Custom Agent");
			System.out.println("* ======================");
			System.err.println("*");
			System.err.println("* !!! WARNING !!!");
			System.err.println("*");
			System.err
					.println("* RMI/JMX port that must be defined with system property: -D"
							+ PORT_KEY
							+ " is missing  ==>  no RMI/JMX server started");
			System.err.println("*");
			System.err
					.println("* Please configure the port correctly to remove this warning.");
			System.err.println("*");
			System.err
					.println("******************************************************");
			System.err.println();
			System.err.println();
			return;
		}

		System.out
				.println("******************************************************");
		System.out.println("*");
		System.out.println("* RMI / JMX Custom Agent");
		System.out.println("* ======================");
		System.out.println("*");

		// Ensure cryptographically strong random number generator used
		// to choose the object number - see java.rmi.server.ObjID
		//
		System.setProperty("java.rmi.server.randomIDs", "true");

		// Start an RMI registry on port specified by example.rmi.agent.port
		// (default 3000).
		//
		final int port = Integer.parseInt(portStr);
		System.out.println("* Create RMI registry on port " + port);
		LocateRegistry.createRegistry(port);

		// Retrieve the PlatformMBeanServer.
		//
		System.out.println("* Get the platform's MBean server");
		MBeanServer mbs = ManagementFactory.getPlatformMBeanServer();

		// Environment map.
		//
		System.out.println("* Initialize the environment map");
		HashMap<String, Object> env = new HashMap<String, Object>();

		// Provide the password file used by the connector server to
		// perform user authentication. The password file is a properties
		// based text file specifying username/password pairs.

		System.out.println("jmx.remote.x.password.file="
				+ System.getProperty(PASSWORD_FILE));
		env.put("jmx.remote.x.password.file", System.getProperty(PASSWORD_FILE));

		// Provide the access level file used by the connector server to
		// perform user authorization. The access level file is a properties
		// based text file specifying username/access level pairs where
		// access level is either "readonly" or "readwrite" access to the
		// MBeanServer operations.

		System.out.println("jmx.remote.x.access.file="
				+ System.getProperty(ACCESS_FILE));
		env.put("jmx.remote.x.access.file", System.getProperty(ACCESS_FILE));
		env.put("jmx.remote.x.authenticate", Boolean.TRUE);

		// Create an RMI connector server.
		//
		// As specified in the JMXServiceURL the RMIServer stub will be
		// registered in the RMI registry running in the local host on
		// port 3000 with the name "jmxrmi". This is the same name the
		// out-of-the-box management agent uses to register the RMIServer
		// stub too.
		//
		// The port specified in "service:jmx:rmi://"+hostname+":"+port
		// is the second port, where RMI connection objects will be exported.
		// Here we use the same port as that we choose for the RMI registry.
		// The port for the RMI registry is specified in the second part
		// of the URL, in "rmi://"+hostname+":"+port
		//
		boolean forceLocalhost = Boolean.getBoolean(FORCE_LOCALHOST_KEY);
		String hostname;
		if (forceLocalhost) {
			System.out
					.println("* Using localhost as hostname as defined by system property: "
							+ FORCE_LOCALHOST_KEY);
			hostname = "localhost";
		} else {
			hostname = InetAddress.getLocalHost().getHostName();
		}

		System.out
				.println("* Create an RMI/JMX connector server, hostname is: "
						+ hostname);
		String serviceUrl = "service:jmx:rmi://" + hostname + ":" + port
				+ "/jndi/rmi://" + hostname + ":" + port + "/jmxrmi";
		System.out.println("*  and url is: " + serviceUrl);
		JMXServiceURL url = new JMXServiceURL(serviceUrl);

		// Now create the server from the JMXServiceURL
		//
		JMXConnectorServer cs = JMXConnectorServerFactory
				.newJMXConnectorServer(url, env, mbs);

		// Start the RMI connector server.
		//
		System.out.println("* Start the RMI/JMX connector server on port "
				+ port);
		cs.start();

		System.out.println("*");
		System.out
				.println("******************************************************");
		System.out.println();
	}
}
